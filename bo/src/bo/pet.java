public class pet {
    
    private String type;
    private String name;
    private String food;
    
    public void setType(String n) {
        type = n;
    }
    public void setName(String n) {
        name = n;
    }
    public void setFood(String n) {
        food = n;
    }
    
    public String getType() {
        return type;
    }
    public String getName() {
        return name;
    }
    public String getFood() {
        return food;
    }    
}
